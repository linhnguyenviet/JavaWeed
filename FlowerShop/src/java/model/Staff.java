package model;

/**
 *
 * @author Tiny
 */
public class Staff {
    int sID;
    String sName;
    String email;
    String password;

    public Staff(int sID, String sName, String email, String password) {
        this.sID = sID;
        this.sName = sName;
        this.email = email;
        this.password = password;
    }

    public int getsID() {
        return sID;
    }

    public void setsID(int sID) {
        this.sID = sID;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
}
